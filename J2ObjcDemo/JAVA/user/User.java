
package user;

public class User{
	
	private int age;

	private String name;

	public User(){

	}

	public void setAge(int age){
		this.age = age;
	}

	public int getAge(){
		return this.age;
	}

	public void setName(String name){
		this.name = name;
	}

	public String getName(){
		return this.name;
	} 
}


